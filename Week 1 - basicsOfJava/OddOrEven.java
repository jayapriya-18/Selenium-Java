public class OddOrEven {
	public static void main(String[] args) {
//		find the number is odd or even
		int i = 10;
		if(i%2==0){
			System.out.println("The given number is Even:"+i);
			
		}
		else {
			System.out.println("The given number is odd:"+i);
		}
	}

}
